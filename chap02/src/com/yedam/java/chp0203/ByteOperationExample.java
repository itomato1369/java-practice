package com.yedam.java.chp0203;

public class ByteOperationExample {

	public static void main(String[] args) {
		// 정수 연산 int가 기초값 
		byte x = 30;
		byte y = 50;
		byte z = (byte)(x + y);
		int q = x + y;
		
		// long 타입이 포함될 경우 long으로 자동변환
		int a = 100;
		long b = 150L;
		long c = a + b;
		
		// parsing 문자를 해당 데이터타입으로 변환
		
	}

}
