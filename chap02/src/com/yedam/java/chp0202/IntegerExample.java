package com.yedam.java.chp0202;

public class IntegerExample {

	public static void main(String[] args) {
		/* 1byte = 8bit*/
		byte v1 = -128;
		byte v2 = -30;
		byte v3 = 0;
		byte v4 = 30;
		// byte v5 = 128; byte의 범위 : -128 ~ 127
		
		/* int   = 4byte */
		int vnum = 9999999;
		
		//  literal은 int로 인식하니 long쓸려면 literal자체를 long타입 L
		//  long  = 8byte
		
		/* long  = 8byte */
		long num1 = 999999999999999999L;
		

	}

}
